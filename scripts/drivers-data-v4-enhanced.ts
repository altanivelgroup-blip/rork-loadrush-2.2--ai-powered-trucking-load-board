export function generateSimulationRoute(
  start: { lat: number; lng: number },
  end: { lat: number; lng: number },
  durationMinutes: number = 5
): Array<{ lat: number; lng: number; timestamp: number }> {
  const steps = 30;
  const route: Array<{ lat: number; lng: number; timestamp: number }> = [];
  const startTime = Date.now();
  const stepDuration = (durationMinutes * 60 * 1000) / steps;

  for (let i = 0; i <= steps; i++) {
    const progress = i / steps;
    const lat = start.lat + (end.lat - start.lat) * progress;
    const lng = start.lng + (end.lng - start.lng) * progress;
    const timestamp = startTime + i * stepDuration;

    route.push({ lat, lng, timestamp });
  }

  return route;
}
